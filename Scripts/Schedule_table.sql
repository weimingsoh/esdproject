  `ID` INT(11) NOT NULL auto_increment,
  `EmployeeID` INT(11) NOT NULL,
  `PlannedStartWork` Datetime NOT NULL,
  `PlannedEndWork` Datetime NOT NULL,
  CONSTRAINT schedule_PK PRIMARY KEY (ID)
);

